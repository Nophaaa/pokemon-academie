export function getBiomeEnLabel(key) {
  if (key.startsWith('#cobblemon:is_')) {
    const raw = key.replace('#cobblemon:is_', '');
    return raw
      .split('_')
      .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
      .join(' ');
  }
  const raw = key.replace(/^[^:]+:/, '');
  return raw
    .split('_')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}
